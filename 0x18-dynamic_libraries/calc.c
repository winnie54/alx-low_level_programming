#include "main.h"

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: return result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * add - adds two integers
 *
 * @a: first input
 * @b: second input
 *
 * Return: sum of a & b
 */
int add(int a, int b)
{
	int sum = a + b;

	return (sum);
}

/**
 * sub - subtracts one integer from another
 * @a: first input
 * @b: second input
 *
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	int sub = a - b;

	return (sub);
}

/**
 * div - divides two integers
 * @a: first input
 * @b: second input
 *
 * Return: return result
 */
int div(int a, int b)
{
	return (a / b);
}
